

<h2>CONCEPT</h2>
<br>
Le but de ce blog est de réunir la synergie de vos idées à tous, afin de créer pour la première fois LA série dont vous serez tous les auteurs.<br>
Nous faisons appel à toute votre créativité et votre imagination.<br>
Serial Blog est un auteur collaboratif, aussi nous vous invitons à construire comme vous le voulez vos commentaires sur chaque idée qui vous intéresse.<br><br>
Chaque fin de mois, vous serez conviés également à voter pour les différentes propositions, qui elles-même, seront directement inspirées de vos commentaires et idées avant d'être synthétisées par notre équipe de scénaristes professionnels.<br><br>
La série sera écrite puis réalisée par notre équipe au rythme d'un épisode par mois. Nous comptons sur vous pour en faire la série web la plus populaire sur le net.<br><br>
Il ne vous reste plus qu'à vous inscrire, puis à passer directement de l'autre côté du rideau... 